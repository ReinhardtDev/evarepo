public class Events {

    public String Bezeichnung;
    public String Ort;
    public String Datum;
    public int Kontingent;

    public Events(String bezeichnung, String ort, String datum, int kontingent) {
        Bezeichnung = bezeichnung;
        Ort = ort;
        Datum = datum;
        Kontingent = kontingent;
    }

}
